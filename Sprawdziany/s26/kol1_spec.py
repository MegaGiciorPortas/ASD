def gentest(n, hint):
    from testy import MY_random
    A = []
    for _ in range(n):
        A.append(1 + (MY_random() % n))
    if n % 5 != 0:
        k = (n * n // 2) + ((MY_random() % (2 * n)) - n)
    else:
        
        k = (n * n) - ((MY_random() % int(n ** 0.5)) + 1) * n
    
    return [A, k], hint
